

-- Tablica Lige
CREATE TABLE lige (
    sifra INT NOT NULL PRIMARY KEY IDENTITY(1,1),
    naziv VARCHAR(50) NOT NULL UNIQUE,
    drzava VARCHAR(50) NOT NULL
);

-- Tablica Klubovi (ime kluba umjesto ID-a u drugim tablicama)
CREATE TABLE klubovi (
    sifra INT NOT NULL PRIMARY KEY IDENTITY(1,1),
    naziv VARCHAR(50) NOT NULL UNIQUE,
    drzava VARCHAR(50),
    liga VARCHAR(50) NOT NULL, -- Spremamo ime lige umjesto ID-a
    FOREIGN KEY (liga) REFERENCES lige(naziv) ON DELETE CASCADE
);

-- Tablica Igrači (ime kluba umjesto ID-a)
CREATE TABLE igraci (
    sifra INT NOT NULL PRIMARY KEY IDENTITY(1,1),
    ime VARCHAR(50) NOT NULL,
    prezime VARCHAR(50) NOT NULL,
    pozicija VARCHAR(30),
    klub VARCHAR(50) NOT NULL, -- Spremamo ime kluba umjesto ID-a
    FOREIGN KEY (klub) REFERENCES klubovi(naziv) ON DELETE CASCADE
);

-- Tablica Utakmice (ime kluba umjesto ID-a)
CREATE TABLE utakmice (
    sifra INT NOT NULL PRIMARY KEY IDENTITY(1,1),
    domaci_klub VARCHAR(50)  not NULL,
    gostujuci_klub VARCHAR(50)  not NULL,
    datum_vrijeme DATETIME NOT NULL,
    rezultat VARCHAR(10),
    FOREIGN KEY (domaci_klub) REFERENCES klubovi(naziv), 
    FOREIGN KEY (gostujuci_klub) REFERENCES klubovi(naziv) 
	);
  
